#!/bin/bash
#===============================================================================
#
#     Rust MC 服务器面板安装器 - Linux 日志工具模块
#
#===============================================================================
# 描述: 提供跨平台一致的日志记录接口，支持多级别日志、彩色控制台输出和文件日志
# 作者: Rust MC 团队
# 版本: 1.0.0
# 使用方法:
#   source logger.sh
#   start_log_session
#   log_info "这是一条信息日志"
#   log_warning "这是一条警告日志"
#   log_error "这是一条错误日志"
#   log_debug "这是一条调试日志"
#===============================================================================

# 防止重复加载
if [ -n "${_LOGGER_LOADED:-}" ]; then
    return 0
fi
readonly _LOGGER_LOADED=1

# 全局日志文件路径
# 默认使用 /tmp 目录下的日志文件，便于排查系统问题
readonly LOG_FILE="/tmp/mc-server-install.log"

# ANSI 转义码定义 - 用于终端彩色输出
# 格式: \033[代码;m  或  \e[代码;m
#-------------------------------------------------------------------------------
# 颜色代码:
#   0;37 - 白色（默认）
#   1;33 - 黄色（粗体）
#   0;31 - 红色
#   1;30 - 灰色（粗体）
#-------------------------------------------------------------------------------
if [ -z "${COLOR_RESET:-}" ]; then
    declare -r COLOR_RESET='\033[0m'       # 重置颜色
    declare -r COLOR_WHITE='\033[0;37m'    # 白色 - INFO 级别
    declare -r COLOR_YELLOW='\033[1;33m'   # 黄色 - WARNING 级别
    declare -r COLOR_RED='\033[0;31m'      # 红色 - ERROR 级别
    declare -r COLOR_GRAY='\033[1;30m'     # 灰色 - DEBUG 级别
fi

#===============================================================================
# 函数: get_timestamp
# 描述: 获取当前时间戳，格式为 yyyy-MM-dd HH:mm:ss
# 返回: 时间戳字符串
#===============================================================================
get_timestamp() {
    date '+%Y-%m-%d %H:%M:%S'
}

#===============================================================================
# 函数: _write_log
# 描述: 内部函数，执行实际的日志写入操作
# 参数:
#   $1 - 日志级别名称（如 INFO, WARNING 等）
#   $2 - 日志消息内容
#   $3 - 颜色代码（ANSI 转义码）
# 返回: 无
#===============================================================================
_write_log() {
    local level="$1"
    local message="$2"
    local color="$3"
    
    # 获取当前时间戳
    local timestamp
    timestamp=$(get_timestamp)
    
    # 格式化日志消息
    local formatted_message="[${timestamp}] [${level}] ${message}"
    
    # 输出到控制台（带颜色）
    echo -e "${color}${formatted_message}${COLOR_RESET}"
    
    # 追加到日志文件
    # 使用双引号包裹路径以处理特殊字符
    echo "$formatted_message" >> "$LOG_FILE" 2>/dev/null || {
        echo "警告: 无法写入日志文件: $LOG_FILE" >&2
    }
}

#===============================================================================
# 函数: log_info
# 描述: 记录信息级别日志
# 参数:
#   $1 - 日志消息内容
# 返回: 无
# 示例:
#   log_info "安装过程开始"
#===============================================================================
log_info() {
    local message="$1"
    _write_log "INFO" "$message" "$COLOR_WHITE"
}

#===============================================================================
# 函数: log_warning
# 描述: 记录警告级别日志
# 参数:
#   $1 - 日志消息内容
# 返回: 无
# 示例:
#   log_warning "检测到配置文件已存在"
#===============================================================================
log_warning() {
    local message="$1"
    _write_log "WARNING" "$message" "$COLOR_YELLOW"
}

#===============================================================================
# 函数: log_error
# 描述: 记录错误级别日志
# 参数:
#   $1 - 日志消息内容
# 返回: 无
# 示例:
#   log_error "无法连接到服务器"
#===============================================================================
log_error() {
    local message="$1"
    _write_log "ERROR" "$message" "$COLOR_RED"
}

#===============================================================================
# 函数: log_debug
# 描述: 记录调试级别日志
# 参数:
#   $1 - 日志消息内容
# 返回: 无
# 示例:
#   log_debug "变量 X 的值为: $x"
#===============================================================================
log_debug() {
    local message="$1"
    _write_log "DEBUG" "$message" "$COLOR_GRAY"
}

#===============================================================================
# 函数: start_log_session
# 描述: 初始化日志会话，创建或清空日志文件
# 参数: 无
# 返回: 无
# 示例:
#   start_log_session  # 在脚本开始时调用
#===============================================================================
start_log_session() {
    # 获取当前时间戳
    local timestamp
    timestamp=$(get_timestamp)
    
    # 创建会话分隔标记
    local session_marker="========================================
日志会话开始: ${timestamp}
========================================"

    # 写入会话开始标记到日志文件（覆盖模式）
    echo "$session_marker" > "$LOG_FILE" 2>/dev/null || {
        echo "错误: 无法创建日志文件: $LOG_FILE" >&2
        return 1
    }
    
    # 输出到控制台
    echo -e "${COLOR_WHITE}[${timestamp}] [INFO] 日志会话已初始化${COLOR_RESET}"
}

# 导出函数供外部脚本使用
# 这样在其他脚本中 source 后可以调用这些函数
export -f log_info
export -f log_warning
export -f log_error
export -f log_debug
export -f start_log_session
export -f get_timestamp
export -f _write_log
